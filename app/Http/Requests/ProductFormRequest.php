namespace App\Http\Requests;

use Illuminate\Support\Facades\Auth;

/**
 * Class ProductFormRequest
 *
 * @package App\Http\Requests
 */
class ProductFormRequest extends Request
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return Auth::check();
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'key-text' => 'required_without:key-select',
            'name-text' => 'required_with:key-text',
            'key-select' => 'required_without:key-text'
        ];
    }

    /**
     * Get the error messages for the defined validation rules.
     *
     * @return array
     */
    public function messages()
    {
        return [
            'required_without' => 'A value must be entered or selected.',
            'required_with' => 'Name is required when entering key using text field.',
        ];
    }
}
